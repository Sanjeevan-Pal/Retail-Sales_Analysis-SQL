--SQL Retail Sales Analysis-I

--CREATING A DATABASE
CREATE DATABASE SQL_PROJECT1
USE SQL_PROJECT1

--CREATING THE TABLE
CREATE TABLE Retail_Sales(
	Transaction_ID INT PRIMARY KEY,
	Sales_Date DATE,
	Sale_time TIME,
	Customer_ID INT,
	Gender VARCHAR(7),
	Age INT,
	Category VARCHAR(15),
	Quantity INT,
	Price_per_unit FLOAT,
	Cogs FLOAT,
	Total_Sales FLOAT
	);

--IMPORTED THE REQUIRD FILE IN THE CURRENT DATABASE

--COPYING THE TABLE VALUES INTO THE TABLE WHICH WE HAVE DEFINED EARLIER
INSERT INTO Retail_Sales
SELECT * 
FROM SQL_PROJECT1.dbo.RETAIL;

